def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sub_of_values(number1, number2):
    result = number1 - number2
    return result

def sum_of_values(number1, number2):
    result = number1 + number2
    return result

def div_of_values(number1, number2):
    result = (number1 / number2)
    return result

def mult_of_values(number1, number2):
    result = number1 * number2
    return result
